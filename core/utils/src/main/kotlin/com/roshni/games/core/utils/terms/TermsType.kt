package com.roshni.games.core.utils.terms

/**
 * Types of terms and conditions documents in the gaming platform
 */
enum class TermsType {
    /**
     * Main terms of service agreement
     */
    TERMS_OF_SERVICE,

    /**
     * Privacy policy document
     */
    PRIVACY_POLICY,

    /**
     * End user license agreement
     */
    EULA,

    /**
     * Community guidelines and rules
     */
    COMMUNITY_GUIDELINES,

    /**
     * Cookie policy and tracking preferences
     */
    COOKIE_POLICY,

    /**
     * Data processing and GDPR compliance
     */
    DATA_PROCESSING_AGREEMENT,

    /**
     * Parental consent and child protection policies
     */
    PARENTAL_CONSENT,

    /**
     * Content usage and licensing terms
     */
    CONTENT_LICENSE,

    /**
     * Payment and subscription terms
     */
    PAYMENT_TERMS,

    /**
     * Third-party service integration terms
     */
    THIRD_PARTY_INTEGRATION,

    /**
     * Custom terms for specific features or regions
     */
    CUSTOM
}